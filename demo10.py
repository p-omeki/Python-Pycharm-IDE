#Making functions return value in Python

def add(a, b): #We first define our function by specifying the arguments.
    c = a+b   #We define a variable the we'll store our arguments.
    return c  #We return the variable that contains our arguments.

result = add(10, 20) #Now, we want our variable c to be returned as a result and so we define variable result which we'll store our c.

print(result) #This will print out the additions of the arguments passed and so the function would have returned a value.