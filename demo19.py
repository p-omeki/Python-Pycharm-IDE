#Numeric functions in python
'''
I'll focus on 3 main numeric functions. That is, minimum, maximum and absolute functions.
Check the example below...
'''
#Min function ~ This will print the minimum number
numbers = 167, 28, 356, 47, 56, 68
print(min(numbers))

#Max function ~ This will print the maximum number
numbers = 167, 28, 356, 47, 56, 68
print(max(numbers))

#Absolute function ~ This will give the absolute number without - or + sign
numbers = -58 #We can only assign one absolute number at a time
print(abs(numbers))